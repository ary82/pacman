package game

import (
	"time"

	tea "github.com/charmbracelet/bubbletea"
)

type Game struct {
	Board  [31][28]int
	Score  int
	Pacman Pacman
	Ghosts [4]*Ghost
}

type Pacman struct {
	XPosition int
	YPosition int
	Direction int // 0: static, 1: up, 2: down, 3: left 4: right
	Lives     int
}

type Ghost struct {
	XPosition               int
	YPosition               int
	ViewCode                int
	IsCurrentPositionPellet bool
	Direction               int // 0: static, 1: up, 2: down, 3: left 4: right
}

type (
	UpdatePacmanPositionMsg int
	UpdateGhostsPositionMsg int
	GameOverMsg             int
)

func MovePacman() tea.Cmd {
	return tea.Every(500*time.Millisecond, func(t time.Time) tea.Msg {
		return UpdatePacmanPositionMsg(0)
	})
}

func MoveGhosts() tea.Cmd {
	return tea.Every(350*time.Millisecond, func(t time.Time) tea.Msg {
		return UpdateGhostsPositionMsg(0)
	})
}

func GameOver() tea.Msg {
	return GameOverMsg(0)
}

